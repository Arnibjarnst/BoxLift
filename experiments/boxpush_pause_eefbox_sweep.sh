#!/bin/bash
# Random-search sweep for the new pause-penalty / EE-box-relative reward mechanics, holding
# dphase_min=0 and enable_phase_slowdown=True fixed. 100 runs, ~6 min each (≈10 h total).
#
# Keeps a normal tracking signal (joint and/or EE) active in addition to the gated
# EE-box-relative reward — the relative reward is silenced during approach/regrasp, so
# without the normal tracker the policy has no shaping signal outside contact phases.
#
# Generator: random.seed(7). Varies:
#   w_total_slowdown       log-uniform [0.005, 0.3]  — quadratic pause penalty weight
#   w_eef_box_rel_pos      uniform [0.0, 1.0]        — gated EE-in-box-frame pos reward
#   w_eef_box_rel_quat     uniform [0.0, 0.5]        — gated EE-in-box-frame quat reward
#   sigma_eef_box_rel_pos  log-uniform [0.02, 0.1]
#   dilation_steps         choice {10, 20}           — pre/post-contact window (±0.2 / ±0.4 s)
#   w_joint_pos            uniform [0.0, 1.0]        — normal joint tracking (active everywhere)
#   w_eef_pos              uniform [0.0, 1.0]        — normal EE pos tracking (active everywhere)

